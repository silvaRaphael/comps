import * as React from "react"
import { type DialogProps } from "@radix-ui/react-dialog"
import { Command as CommandPrimitive } from "cmdk"

import { cn } from "@comps/lib"
import { Dialog, DialogContent } from "../dialog"
import { Loader2, Search } from "lucide-react"

const Command = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive>
>(({ className, ...props }, ref) => (
  <CommandPrimitive
    ref={ref}
    className={cn(
      "flex h-full w-full flex-col overflow-hidden rounded-md bg-popover text-popover-foreground focus:border-transparent focus:outline-none focus:ring-transparent",
      className
    )}
    {...props}
  />
))
Command.displayName = CommandPrimitive.displayName

export interface CommandDialogProps extends DialogProps {}

const CommandDialog = ({ children, ...props }: CommandDialogProps) => {
  return (
    <Dialog {...props}>
      <DialogContent className="overflow-hidden p-0">
        <Command className="[&_[cmdk-group-heading]]:px-2 [&_[cmdk-group-heading]]:font-medium [&_[cmdk-group-heading]]:text-muted-foreground [&_[cmdk-group]:not([hidden])_~[cmdk-group]]:pt-0 [&_[cmdk-group]]:px-2 [&_[cmdk-input-wrapper]_svg]:h-5 [&_[cmdk-input-wrapper]_svg]:w-5 [&_[cmdk-input]]:h-12 [&_[cmdk-item]]:px-2 [&_[cmdk-item]]:py-3 [&_[cmdk-item]_svg]:h-5 [&_[cmdk-item]_svg]:w-5">
          {children}
        </Command>
      </DialogContent>
    </Dialog>
  )
}

const CommandInput = React.forwardRef<
  React.ElementRef<typeof CommandPrimitive.Input>,
  React.ComponentPropsWithoutRef<typeof CommandPrimitive.Input> & {
    wrapperClassName?: string
    loading?: boolean
  }
>(({ className, wrapperClassName, loading, ...props }, ref) => (
  <div
    className={cn("relative w-full", wrapperClassName)}
    cmdk-input-wrapper=""
  >
    {loading ? (
      <Loader2 className="absolute left-2.5 top-2 z-[1] size-4 animate-spin text-gray-400" />
    ) : (
      <Search
        strokeWidth={2.5}
        className="absolute left-2.5 top-2 z-[1] size-4 text-gray-500"
      />
    )}
    <CommandPrimitive.Input
      ref={ref}
      className={cn(
        "h-8 w-full rounded-md border text-sm border-gray-100 text-gray-800 placeholder-gray-500 bg-gray-100/75 focus:border-transparent focus:outline-none focus:ring-transparent ps-8",
        className
      )}
      {...props}
    />
  </div>
))

CommandInput.displayName = CommandPrimitive.Input.displayName

